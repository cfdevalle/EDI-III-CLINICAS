<h4> Reseña Institucional </h4>
<p>La clínica NFSS fue construida en 1950, con una estructura funcional y visionaria para la época, que aún hoy se mantiene vigente.
A partir del mes de octubre de 1999 se produce un cambio en la gestión de dirección, y se establece como misión principal “brindar una calidad medica de excelencia”.
NFSS  considera de suma importancia realizar un sistemático control y seguimiento de los pacientes, como así también, la planificación a medio y largo plazo con el fin de mejorar los servicios brindados, y de introducir otros, en función a las necesidades de sus pacientes.
La clínica NFSS cuenta con tecnología de avanzada y un plantel de médicos capacitados 
 </p>
 <h4>Mision</h4>
 <img src="static/img/doctor1.jpg">
 <p>Brindar a la familia una amplia variedad de servicios de asistencia medica, con una atencion personalizada, calidad medica y etica profesional</p>
 <h4>Mision</h4>
  <p>Ser una clinica que brinde calidad medica y humana, logrando un elevado confort del paciente</p>
